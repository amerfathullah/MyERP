    #region Posting Dimension Validation Integration

    [Fact]
    public void JournalEntryLine_CostCenterId_IsBuiltInDimension()
    {
        var line = new JournalEntryLine(Guid.NewGuid(), Guid.NewGuid(), Guid.NewGuid(), 1000m, true);
        var ccId = Guid.NewGuid();
        line.CostCenterId = ccId;

        line.CostCenterId.ShouldBe(ccId);
    }

    [Fact]
    public void JournalEntryLine_ProjectId_IsBuiltInDimension()
    {
        var line = new JournalEntryLine(Guid.NewGuid(), Guid.NewGuid(), Guid.NewGuid(), 500m, false);
        var projectId = Guid.NewGuid();
        line.ProjectId = projectId;

        line.ProjectId.ShouldBe(projectId);
    }

    [Fact]
    public void JournalEntry_AllLinesHaveDimensions_PassesValidation()
    {
        // If all lines have the mandatory dimension set, validation passes
        var je = new JournalEntry(Guid.NewGuid(), Guid.NewGuid(), Guid.NewGuid(), DateTime.Today);
        var accId = Guid.NewGuid();
        je.AddLine(accId, 1000m, true);
        je.AddLine(accId, 1000m, false);

        // Set cost center on all lines
        foreach (var line in je.Lines)
            line.CostCenterId = Guid.NewGuid();

        je.Lines.All(l => l.CostCenterId.HasValue).ShouldBeTrue();
    }

    [Fact]
    public void JournalEntry_MissingDimensionOnOneLine_FailsValidation()
    {
        // If one line is missing a mandatory dimension, validation should fail
        var je = new JournalEntry(Guid.NewGuid(), Guid.NewGuid(), Guid.NewGuid(), DateTime.Today);
        var accId = Guid.NewGuid();
        je.AddLine(accId, 1000m, true);
        je.AddLine(accId, 1000m, false);

        // Only set cost center on first line
        je.Lines[0].CostCenterId = Guid.NewGuid();
        // Second line has no cost center

        je.Lines.Any(l => !l.CostCenterId.HasValue).ShouldBeTrue(); // At least one missing
    }

