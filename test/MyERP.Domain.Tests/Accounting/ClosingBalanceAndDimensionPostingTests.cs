    [Fact]
    public void AccountingDimensionFilter_AllowMode_PermitsListedValues()
    {
        var filterId = Guid.NewGuid();
        var dimId = Guid.NewGuid();
        var accountId = Guid.NewGuid();
        var companyId = Guid.NewGuid();
        var allowedValue = Guid.NewGuid();

        var filter = new AccountingDimensionFilter(
            filterId, dimId, accountId, companyId, isAllowList: true);
        filter.DimensionValueIds = allowedValue.ToString();

        filter.IsValuePermitted(allowedValue).ShouldBeTrue();
        filter.IsValuePermitted(Guid.NewGuid()).ShouldBeFalse(); // Not in allow list
    }

    [Fact]
    public void AccountingDimensionFilter_BlockMode_DeniesListedValues()
    {
        var filterId = Guid.NewGuid();
        var dimId = Guid.NewGuid();
        var accountId = Guid.NewGuid();
        var companyId = Guid.NewGuid();
        var blockedValue = Guid.NewGuid();

        var filter = new AccountingDimensionFilter(
            filterId, dimId, accountId, companyId, isAllowList: false);
        filter.DimensionValueIds = blockedValue.ToString();

        filter.IsValuePermitted(blockedValue).ShouldBeFalse(); // Blocked
        filter.IsValuePermitted(Guid.NewGuid()).ShouldBeTrue(); // Not in block list = allowed
    }

    [Fact]
    public void AccountingDimensionFilter_EmptyAllowList_BlocksAll()
    {
        var filter = new AccountingDimensionFilter(
            Guid.NewGuid(), Guid.NewGuid(), Guid.NewGuid(), Guid.NewGuid(),
            isAllowList: true);
        // Empty allow list = nothing is permitted

        filter.IsValuePermitted(Guid.NewGuid()).ShouldBeFalse();
    }

    [Fact]
    public void AccountingDimensionFilter_EmptyBlockList_AllowsAll()
    {
        var filter = new AccountingDimensionFilter(
            Guid.NewGuid(), Guid.NewGuid(), Guid.NewGuid(), Guid.NewGuid(),
            isAllowList: false);
        // Empty block list = everything is permitted

        filter.IsValuePermitted(Guid.NewGuid()).ShouldBeTrue();
    }

